-- I18n foundation: languages + translation keys

-- Align languages table with i18n contract
ALTER TABLE core.languages RENAME COLUMN title TO name;
ALTER TABLE core.languages RENAME COLUMN enabled TO is_active;
ALTER TABLE core.languages ADD COLUMN IF NOT EXISTS is_default boolean NOT NULL DEFAULT false;

CREATE UNIQUE INDEX IF NOT EXISTS ux_languages_default
  ON core.languages (is_default)
  WHERE is_default = true;

-- Seed base languages (default = en)
INSERT INTO core.languages (code, name, is_active, is_default)
VALUES
  ('en', 'English', true, true),
  ('ru', 'Русский', true, false)
ON CONFLICT (code) DO UPDATE
  SET name = EXCLUDED.name,
      is_active = EXCLUDED.is_active,
      is_default = EXCLUDED.is_default;

UPDATE core.languages
SET is_default = CASE WHEN code = 'en' THEN true ELSE false END;

-- Translation key registry
CREATE TABLE core.i18n_keys (
  key VARCHAR(128) PRIMARY KEY,
  description TEXT,
  created_at TIMESTAMP NOT NULL DEFAULT now()
);

-- Translations bound to keys + languages
CREATE TABLE core.i18n_translations (
  key VARCHAR(128) NOT NULL,
  language_code VARCHAR(8) NOT NULL,
  value TEXT NOT NULL,

  PRIMARY KEY (key, language_code),
  FOREIGN KEY (key) REFERENCES core.i18n_keys(key) ON DELETE CASCADE,
  FOREIGN KEY (language_code) REFERENCES core.languages(code) ON DELETE CASCADE
);

